public class Task1 {

	/**
	 * This method converts <b>Celsius</b> to <b>Kelvin</b>.
	 * 
	 * @param C
	 *            - represents Celsius degrees.
	 * @return Celsius converted to Kelvin's.
	 */

	public static double convertToKelvins(double C) {
		double K = 0;

		return K = 273.15 + C;
	}

	public static void main(String[] args) {

		System.out.println(convertToKelvins(100));

	}

}
